    private void jbGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbGuardarActionPerformed

try{
             Integer id= Integer.parseInt(jtCodigo.getText());
             String nombre = jtNombre.getText();
             String a単o = jtA単o.getText();
             if(nombre.isEmpty() || a単o.isEmpty()){
                 JOptionPane.showMessageDialog(this,"No puede haber campos vacios");
                 return;
             }
                    
             
             Boolean estado = jrEstado.isSelected();
             if(materiaActual==null){
                 materiaActual =new Materia (id,nombre,Integer.parseInt(a単o),estado);
                 mateData.guardarMateria(materiaActual);
             }
             
             else{
                 materiaActual.setIdMateria(id);
                 materiaActual.setNombre(nombre);
                 materiaActual.setAnioMateria(Integer.parseInt(a単o));
                 mateData.modificarMateria(materiaActual);
             }
                
             
             
        }catch(NumberFormatException ex){
            JOptionPane.showMessageDialog(this,"Debe ingresar un n炭mero v叩lido");
        }
            
        
        
    }//GEN-LAST:event_jbGuardarActionPerformed

    
    
    
